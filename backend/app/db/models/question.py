"""
app/db/models/question.py

Question Bank models for Mindexa.

Tables defined here:
    question                     — A reusable question owned by a lecturer
    question_option              — Answer options for closed question types
    question_blank               — Blank definitions for fill-in-the-blank questions
    assessment_question           — Junction: question ↔ assessment (with per-assessment overrides)
    ai_generation_batch          — A single AI generation request session
    ai_generated_question        — A question candidate generated by AI
    ai_question_review           — Lecturer decision on each AI-generated candidate
    question_bank_entry           — The lecturer's question bank view
"""

import uuid
from datetime import datetime
from typing import TYPE_CHECKING, Optional

from sqlalchemy import Column, ForeignKey, Index, UniqueConstraint, text
from sqlalchemy.dialects.postgresql import JSONB, UUID
from sqlmodel import Field, Relationship

from app.core.constants import GradingMode
from app.db.base import BaseModel
from app.db.enums import (
    AIBatchStatus,
    AIQuestionDecision,
    DifficultyLevel,
    QuestionAddedVia,
    QuestionSourceType,
    QuestionType,
)
from app.db.mixins import composite_index

if TYPE_CHECKING:
    from app.db.models.assessment import Assessment, AssessmentSection, Rubric
    from app.db.models.attempt import StudentResponse


# ─────────────────────────────────────────────────────────────────────────────
# QUESTION
# ─────────────────────────────────────────────────────────────────────────────


class Question(BaseModel, table=True):
    """
    A reusable question owned by a lecturer.
    """

    __tablename__ = "question"

    __table_args__ = (
        # Fast subject-filtered bank queries
        composite_index("question", "subject_id", "question_type", "is_deleted"),
        composite_index("question", "subject_id", "difficulty", "is_deleted"),
        composite_index("question", "source_type", "is_approved", "is_deleted"),
        # Lecturer's own questions view
        composite_index("question", "created_by_id", "is_in_question_bank"),
        # Source traceability
        composite_index("question", "source_assessment_id", "source_type"),
        composite_index("question", "source_ai_batch_id"),
        # Parent-child lineage
        composite_index("question", "parent_question_id"),
    )

    created_by_id: uuid.UUID = Field(nullable=False, index=True)

    subject_id: uuid.UUID | None = Field(
        default=None,
        sa_column=Column(
            UUID(as_uuid=True),
            ForeignKey("subject.id", ondelete="SET NULL"),
            nullable=True,
            index=True,
        ),
    )

    question_type: QuestionType = Field(nullable=False, index=True)
    content: str = Field(nullable=False)
    explanation: str | None = Field(default=None, nullable=True)

    marks: int = Field(default=1, nullable=False)
    difficulty: DifficultyLevel = Field(default=DifficultyLevel.MEDIUM, nullable=False, index=True)
    grading_mode: str = Field(default=GradingMode.AUTO.value, nullable=False, index=True)
    topic_tag: str | None = Field(default=None, nullable=True, max_length=100, index=True)

    rubric_id: uuid.UUID | None = Field(
        default=None,
        sa_column=Column(
            UUID(as_uuid=True),
            ForeignKey("rubric.id", ondelete="SET NULL"),
            nullable=True,
            index=True,
        ),
    )

    source_type: QuestionSourceType = Field(
        default=QuestionSourceType.MANUAL, nullable=False, index=True
    )
    source_assessment_id: uuid.UUID | None = Field(
        default=None,
        sa_column=Column(
            UUID(as_uuid=True),
            ForeignKey("assessment.id", ondelete="SET NULL"),
            nullable=True,
            index=True,
        ),
    )
    source_ai_batch_id: uuid.UUID | None = Field(default=None, nullable=True)
    ai_action_log_id: uuid.UUID | None = Field(default=None, nullable=True, index=True)

    is_approved: bool = Field(default=False, nullable=False, index=True)
    approved_by_id: uuid.UUID | None = Field(default=None, nullable=True)
    approved_at: datetime | None = Field(default=None, nullable=True)
    is_shared: bool = Field(default=False, nullable=False, index=True)
    is_active: bool = Field(default=True, nullable=False)

    is_in_question_bank: bool = Field(default=False, nullable=False)
    bank_added_at: datetime | None = Field(default=None, nullable=True)
    bank_added_by_id: uuid.UUID | None = Field(default=None, nullable=True)

    version: int = Field(default=1, nullable=False)
    parent_question_id: uuid.UUID | None = Field(
        default=None,
        sa_column=Column(
            UUID(as_uuid=True),
            ForeignKey("question.id", ondelete="SET NULL"),
        ),
    )

    # ── Relationships ─────────────────────────────────────────────────────────

    rubric: Optional["Rubric"] = Relationship()

    options: list["QuestionOption"] = Relationship(
        back_populates="question",
        sa_relationship_kwargs={
            "cascade": "all, delete-orphan",
            "order_by": "QuestionOption.order_index",
        },
    )
    blanks: list["QuestionBlank"] = Relationship(
        back_populates="question",
        sa_relationship_kwargs={
            "cascade": "all, delete-orphan",
            "order_by": "QuestionBlank.blank_index",
        },
    )
    assessment_questions: list["AssessmentQuestion"] = Relationship(back_populates="question")
    bank_entry: Optional["QuestionBankEntry"] = Relationship(
        back_populates="question",
        sa_relationship_kwargs={"cascade": "all, delete-orphan"},
    )
    responses: list["StudentResponse"] = Relationship(back_populates="question")


